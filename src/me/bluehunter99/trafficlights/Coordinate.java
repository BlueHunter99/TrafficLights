package me.bluehunter99.trafficlights;

/**
 * The Coordinate class represents a simple coordinate.
 *
 * @author BlueHunter99
 *
 */

public class Coordinate {

    // Variables
    public int x, y;

    /**
     * Constructor
     *
     * @param x: x position
     * @param y: y position
     */
    public Coordinate(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
