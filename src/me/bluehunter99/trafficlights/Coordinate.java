package me.bluehunter99.trafficlights;

/**
 * De Coordinate klasse is een een klasse om een simpel coordinaat te hebben.
 *
 * @author Joeri
 *
 */

public class Coordinate {

    // Variabelen
    public int x, y;

    /**
     * Constructor
     *
     * @param x:
     *            x positie
     * @param y:
     *            y positie
     */
    public Coordinate(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
